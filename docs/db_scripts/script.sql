CREATE OR REPLACE VIEW v_productos AS
SELECT
  p.id,
  p.nombre,
  p.precio,
  c.nombre AS categoria,
  p.activo,
  p.updated_at
FROM productos p
JOIN categorias c ON c.id = p.categoria_id
ORDER BY c.nombre, p.nombre;

-- ============================================================
-- 6. Función helper: buscar por nombre (útil para la IA)
-- ============================================================

CREATE OR REPLACE FUNCTION buscar_producto(query TEXT)
RETURNS TABLE(id INT, nombre TEXT, precio NUMERIC, categoria TEXT) AS $$
  SELECT p.id, p.nombre, p.precio, c.nombre
  FROM productos p
  JOIN categorias c ON c.id = p.categoria_id
  WHERE p.activo = TRUE
    AND p.nombre ILIKE '%' || query || '%'
  ORDER BY p.nombre
  LIMIT 50;
$$ LANGUAGE sql STABLE;

